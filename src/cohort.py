"""
Synthetic cohort generation for AI DR screening evaluation.

Generates a patient-level dataset representing the NZ diabetic screening
population, with demographics, clinical status, and simulated AI predictions.
All generation parameters are drawn from published literature — see
config/parameters.yaml for sources.
"""

import numpy as np
import pandas as pd
import yaml
from pathlib import Path
from scipy.stats import norm
from scipy.special import expit


def load_parameters(config_path=None):
    """Load simulation parameters from the YAML config file."""
    if config_path is None:
        config_path = Path(__file__).parent.parent / "config" / "parameters.yaml"
    with open(config_path, "r") as f:
        return yaml.safe_load(f)


def generate_cohort(params, seed=None):
    """
    Generate a synthetic cohort of patients attending DR screening.

    Returns a DataFrame with one row per patient, including demographics
    (ethnicity, sex, age, NZDep quintile) and true DR status.

    Parameters
    ----------
    params : dict
        Loaded parameter dictionary (from load_parameters).
    seed : int, optional
        Random seed. Defaults to the value in params.
    """
    if seed is None:
        seed = params["cohort"]["random_seed"]
    rng = np.random.default_rng(seed)
    n = params["cohort"]["n_patients"]

    demo = params["demographics"]
    clin = params["clinical"]

    # --- Ethnicity ---
    eth_names = list(demo["ethnicity_proportions"].keys())
    eth_probs = list(demo["ethnicity_proportions"].values())
    ethnicity = rng.choice(eth_names, size=n, p=eth_probs)

    # --- Sex ---
    p_female = demo["sex_female_proportion"]
    sex = rng.choice(["Female", "Male"], size=n, p=[p_female, 1 - p_female])

    # --- Age (truncated normal) ---
    age_raw = rng.normal(demo["age"]["mean"], demo["age"]["std"], size=n)
    age = np.clip(age_raw, demo["age"]["min"], demo["age"]["max"]).astype(int)

    # --- NZDep quintile (conditional on ethnicity) ---
    nzdep = np.empty(n, dtype=int)
    for eth in eth_names:
        mask = ethnicity == eth
        if mask.any():
            nzdep[mask] = rng.choice(
                [1, 2, 3, 4, 5],
                size=mask.sum(),
                p=demo["nzdep_by_ethnicity"][eth],
            )

    # --- DR status ---
    # Two-stage: first assign any-DR, then among those assign referable DR.
    has_any_dr = np.zeros(n, dtype=bool)
    has_referable_dr = np.zeros(n, dtype=bool)

    for eth in eth_names:
        mask = ethnicity == eth
        count = mask.sum()
        if count == 0:
            continue

        p_any = clin["any_dr_prevalence"][eth]
        p_ref = clin["referable_dr_prevalence"][eth]

        has_any_dr[mask] = rng.random(count) < p_any

        # Among those with any DR, probability of being referable
        dr_sub = mask & has_any_dr
        if dr_sub.any():
            p_ref_given_dr = min(p_ref / p_any, 1.0)
            has_referable_dr[dr_sub] = rng.random(dr_sub.sum()) < p_ref_given_dr

    df = pd.DataFrame(
        {
            "patient_id": np.arange(1, n + 1),
            "ethnicity": ethnicity,
            "sex": sex,
            "age": age,
            "nzdep_quintile": nzdep,
            "has_any_dr": has_any_dr,
            "has_referable_dr": has_referable_dr,
        }
    )
    return df


def _generate_scores(is_positive, sensitivity, specificity, rng):
    """
    Produce continuous AI probability scores using a binormal signal-detection
    model calibrated to the given sensitivity and specificity.

    Positive cases receive scores drawn from N(d'/2, 1), negative from
    N(-d'/2, 1), with d' = z(sensitivity) + z(specificity). Scores are
    passed through a logistic transform to yield values in (0, 1).
    """
    z_sens = norm.ppf(np.clip(sensitivity, 0.01, 0.99))
    z_spec = norm.ppf(np.clip(specificity, 0.01, 0.99))
    d_prime = z_sens + z_spec

    pos = np.array(is_positive, dtype=bool)
    n = len(pos)
    latent = np.empty(n)
    latent[pos] = rng.normal(d_prime / 2, 1.0, size=pos.sum())
    latent[~pos] = rng.normal(-d_prime / 2, 1.0, size=(~pos).sum())

    scores = expit(latent)
    return scores


def simulate_predictions(df, params, tool_key="tool_a", scenario="equal"):
    """
    Simulate AI screening predictions for each patient.

    Parameters
    ----------
    df : DataFrame
        Cohort generated by generate_cohort.
    params : dict
        Loaded parameters.
    tool_key : str
        Which AI tool profile to use ('tool_a', 'tool_b', 'tool_c').
    scenario : str
        'equal' — uniform performance across subgroups.
        'differential' — performance varies by ethnicity and deprivation.

    Returns
    -------
    DataFrame with added columns: ai_score, ai_prediction, tool_name, scenario.
    """
    rng = np.random.default_rng(params["cohort"]["random_seed"] + hash(tool_key) % 1000)

    tool = params["ai_tools"][tool_key]
    base_sens = tool["base_sensitivity"]
    base_spec = tool["base_specificity"]

    df = df.copy()
    n = len(df)

    if scenario == "equal":
        scores = _generate_scores(
            df["has_referable_dr"].values, base_sens, base_spec, rng
        )
    elif scenario == "differential":
        mods = params["equity_scenarios"]["differential"]
        scores = np.empty(n)

        # Process each ethnicity x nzdep combination for vectorised generation
        for eth in df["ethnicity"].unique():
            for q in df["nzdep_quintile"].unique():
                mask = (df["ethnicity"] == eth) & (df["nzdep_quintile"] == q)
                if not mask.any():
                    continue

                s_mod = mods["sensitivity_by_ethnicity"].get(eth, 1.0)
                s_dep = mods["sensitivity_by_nzdep"].get(q, 1.0)
                p_mod = mods["specificity_by_ethnicity"].get(eth, 1.0)
                p_dep = mods["specificity_by_nzdep"].get(q, 1.0)

                adj_sens = min(base_sens * s_mod * s_dep, 0.999)
                adj_spec = min(base_spec * p_mod * p_dep, 0.999)

                scores[mask.values] = _generate_scores(
                    df.loc[mask, "has_referable_dr"].values,
                    adj_sens,
                    adj_spec,
                    rng,
                )
    else:
        raise ValueError(f"Unknown scenario: {scenario}")

    # Binary prediction at the 0.5 threshold (standard operating point)
    df["ai_score"] = scores
    df["ai_prediction"] = scores >= 0.5
    df["tool_name"] = tool["name"]
    df["scenario"] = scenario

    return df
